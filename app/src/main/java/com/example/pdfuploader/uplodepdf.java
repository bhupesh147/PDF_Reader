package com.example.pdfuploader;

public class uplodepdf {

    public String name;
    public String url;

    public uplodepdf() {
    }

    public uplodepdf(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }


    public String getUrl() {
        return url;
    }

}
